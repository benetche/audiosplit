export type TranslationKey = keyof typeof en;

const dictionaries: Record<AppLanguage, Record<TranslationKey, string>> = {
  en,
  "pt-BR": ptBr
};

const interpolate = (template: string, params?: Params): string => {
  if (!params) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => String(params[key] ?? ""));
};

export const translate = (language: AppLanguage, key: TranslationKey, params?: Params): string => {
  const dictionary = dictionaries[language] ?? dictionaries.en;
  return interpolate(dictionary[key], params);
};

